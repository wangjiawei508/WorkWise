#!/usr/bin/env bash
# init-archive-tree.sh
# 在指定根目录下初始化运营监测项目的 13 大类归档骨架。
# 用法: ./init-archive-tree.sh <项目根目录>
# 例:   ./init-archive-tree.sh ./XX地铁X号线运营监测

set -euo pipefail

ROOT="${1:-.}"
mkdir -p "$ROOT"
cd "$ROOT"

DIRS=(
  "01_公司与人员资质"
  "02_仪器设备报审"
  "03_监测方案/3.1_前期设计资料"
  "03_监测方案/3.2_最终方案"
  "03_监测方案/3.3_项目合同"
  "04_初始值报审/4.1_测点埋设及验收"
  "04_初始值报审/4.2_首期控制网联测报告"
  "04_初始值报审/4.3_监测项目原始记录"
  "04_初始值报审/4.4_初始值报告"
  "05_监测日报表/5.1_轨道交通本体监测报表"
  "05_监测日报表/5.2_基坑抽测监测报表"
  "06_巡视巡查报表"
  "07_原始记录/7.1_沉降监测"
  "07_原始记录/7.2_水平位移监测"
  "07_原始记录/7.3_环片收敛监测"
  "07_原始记录/7.4_三维激光扫描"
  "07_原始记录/7.5_基坑监测"
  "08_监测周报"
  "09_监测月报"
  "10_控制网联测/10.1_高程控制网联测报告"
  "10_控制网联测/10.2_平面控制网联测报告"
  "11_监测报告/11.1_阶段性报告"
  "11_监测报告/11.2_年度报告"
  "11_监测报告/11.3_总结报告"
  "11_监测报告/11.4_期监测报告"
  "12_过程控制资料/12.1_安全技术交底"
  "12_过程控制资料/12.2_仪器设备检定报告"
  "12_过程控制资料/12.3_仪器维护记录"
  "12_过程控制资料/12.4_水准仪i角检验记录"
  "12_过程控制资料/12.5_全站仪检核记录"
  "12_过程控制资料/12.6_其他仪器检核记录"
  "12_过程控制资料/12.7_培训会议记录"
  "13_往来文件/13.1_业务联系单"
  "13_往来文件/13.2_变更单"
  "13_往来文件/13.3_预警与消警记录"
)

for d in "${DIRS[@]}"; do
  mkdir -p "$d"
  touch "$d/.gitkeep"
done

cat > README.md <<'EOF'
# 运营监测档案根目录

按 13 大类归档骨架组织。每期次在对应子目录下建立 `YYYY-MM-期次号_<类别>` 子文件夹。
文件命名：`<项目编号>_<期次>_<类别>_<上下行/范围>_<版本>.<扩展>`
原始数据当日入库；平差工程文件与报告 PDF 一并保存。
EOF

echo "归档骨架已生成于：$(pwd)"
find . -type d | sort
